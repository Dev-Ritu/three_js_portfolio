    deskScale: isSmall ? 0.05 : isMobile ? 0.06 : 0.065,
    deskPosition: isMobile ? [-3, -4.5, 0] : [-6, -5.5, 0],
    cubePosition: isSmall ? [-4, -5, 0] : isMobile ? [4, -5, 0] : isTablet ? [6, -5, 0] : [16, -5.5, 0],
    reactLogoPosition: isSmall ? [8, 3, 0] : isMobile ? [14, 4, 0] : isTablet ? [-4, 4, 0] : [-16, 3, 0],
    JSLogoPosition: isSmall ? [4, 0.5, 0] : isMobile ? [14, 2, 0] : isTablet ? [-4, 2, 0] : [-16, 0.5, 0],
    ringPosition: isSmall ? [-15, 7, 0] : isMobile ? [-12, 10, 0] : isTablet ? [1, 10, 0] : [20, 15, 0],
